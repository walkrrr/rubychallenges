weather = 'sunny'
case weather
when 'cloudy'
  puts "It looks gloomy outside. Wear a hoodie."
when 'foggy'
  puts "It's going to be tough to see the roads today. Be careful and carry a flashlight!"
when 'rainy'
  puts "It's going to rain today. Put on a raincoat and bring an umbrella!"
else
puts "Hmm, well I don't know what the forecast is today. Wear a t-shirt and carry an umbrella just in case."
end
